//switch statement
var season=prompt("please enter the season");

switch(season){
	case"winter":
	console.log("this is winter time");
	break;
	case"summer":
	console.log("this is summer time");
	break;
	case"autumn":
	console.log("this is autumn time");
	break;
	case"spring":
	console.log("this is spring time");
	break;
	default:
	console.log("you have given a wrong input");
}